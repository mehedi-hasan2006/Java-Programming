public class Dayy_11 {
    public static void main(String[] args) {

        int sum=15;                      // 0+1=1    15, 14 13 12
        int counter=6;
        while (counter<=5){
            sum=sum+counter;
            System.out.println(sum);
            counter++;
        }

        int summ=0;
        for (int i=1; i<=5; i++){
            summ=summ+i;
            System.out.println(summ);
        }

        int a=10;
        while (a>=1){
            System.out.println("sorry");
            a--;
        }
    }
}
